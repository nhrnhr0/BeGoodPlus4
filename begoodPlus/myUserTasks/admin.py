from django.contrib import admin

# Register your models here.
from .models import ContactFormTask,ProductsTask
class ContactFormTaskAdmin(admin.ModelAdmin):
    list_display = ('id','submited', 'session','task_name', 'created_date','modified_date','name','phone','email', 'message', 'url')

admin.site.register(ContactFormTask, ContactFormTaskAdmin)

class ProductsTaskAdmin(admin.ModelAdmin):
    list_display = ('id', 'submited', 'session', 'task_name', 'created_date','modified_date','name','phone','email')
    filter_horizontal = ('products',)
admin.site.register(ProductsTask,ProductsTaskAdmin)